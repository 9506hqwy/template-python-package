"""
Arithmetics function.
"""


def add(a: int, b: int) -> int:
    """Addition operation.

    :param a: Operand 1.
    :type a: int
    :param b: Operand 2.
    :type b: int
    :return: Result.
    :rtype: int
    """
    return a + b


def sub(a: int, b: int) -> int:
    """Subtraction operation.

    :param a: Operand 1.
    :type a: int
    :param b: Operand 2.
    :type b: int
    :return: Result.
    :rtype: int
    """
    return a - b
